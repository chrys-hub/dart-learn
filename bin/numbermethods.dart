void main(List<String> args) {
  var a = -2;
  print(a.abs());
  print(a.ceil());
  print(a.compareTo(-2));
  print(a.compareTo(2));
  print(a.compareTo(-3));
  print(a.floor());
  print(a.remainder(2));
  print(a.round());
  print(a.toDouble());
  print(a.toInt());
  print(a.toString());
  print(a.truncate());
}
